from math import pi
class circle():
    def get_radius(self):
        self.r=int(input("Enter the radius"))
    def get_area(b):
        val=pi*(b.r)**2
        print("Area is :%.2f"%val)
    def get_perimeter(c):
        ans=2*pi*(c.r)
        print("Circle's perimeter is :%.2f"%ans)
p=circle()
p.get_radius()
p.get_area()
p.get_perimeter()
        
